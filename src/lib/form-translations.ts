/**
 * Get translated label based on language code.
 * Returns the translation for the primary language, falling back to English.
 */
export function getFormLabel(en: string, lang: string): string {
  switch (lang) {
    case "RO/SE":
      return FORM_LABELS_RO[en] || en;
    case "TH/SE":
      return FORM_LABELS_TH[en] || en;
    case "SE":
      // For SE-only, we don't show English at all — caller handles this
      return en;
    case "EN/SE":
    default:
      return en;
  }
}

/**
 * Get the Swedish label for a section header.
 * For SE-only mode, we skip the English entirely.
 */
export function getFormSectionLabel(en: string, sv: string, lang: string): string {
  switch (lang) {
    case "SE":
      return sv;
    case "RO/SE": {
      const ro = FORM_LABELS_RO[en] || en;
      return `${ro} / ${sv}`;
    }
    case "TH/SE": {
      const th = FORM_LABELS_TH[en] || en;
      return `${th} / ${sv}`;
    }
    case "EN/SE":
    default:
      return `${en} / ${sv}`;
  }
}
